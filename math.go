package main

import "fmt"

func main() {
	//Basic values
	numOne := 10
	numbTwo := 2

	//Basic math operations
	add := numOne + numbTwo
	sub := numOne - numbTwo
	mult := numOne * numbTwo
	div := numOne / numbTwo
	mod := numOne % numbTwo

	//Output
	fmt.Println("addition:", add)
	fmt.Println("Subtraction:", sub)
	fmt.Println("Multiplication:", mult)
	fmt.Println("Division:", div)
	fmt.Println("Module:", mod)

}
